//Operadores de Atribuição

var num1 = 3;
num1 = 7;
num1 += 10; // num1 = num1 + 10; a variavel soma o valor nela armazenada e mais o valor 10
num1 -= 3; // num1 = num1 - 3;
num1 *= 5; // num1 = num1 * 5;
num1 /= 2; // num1 = num1 / 2;

Console.WriteLine(num1);

int a = 1;
int b = a;

a++; // a = a + 1 incrementa mais 1
b--; // b = b - 1 decrementa menos 1

Console.WriteLine($"{a} {b}");
